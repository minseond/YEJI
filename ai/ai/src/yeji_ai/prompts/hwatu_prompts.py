# ============================================================
# 프롬프트 빌더 함수
# ============================================================

def build_hwatu_reading_prompt(
    cards: list[dict],
    question: str | None = None,
    lite: bool = False,
) -> str:
    """화투 점 프롬프트 빌드

    Args:
        cards: 뽑힌 카드 정보 리스트 (4개)
            각 카드는 다음 필드 포함:
            - position: SELF | OTHER | PROCESS | RESULT
            - card_code: 카드 코드 (예: JAN_GWANG, FEB_YULKKOT)
            - card_name: 카드 한글명 (예: "송학 광", "매 열끗")
            - card_month: 월 (1-12)
            - card_type: 카드 등급 ("광" | "열끗" | "띠" | "피")
        question: 사용자 질문 (선택)
        lite: True면 경량화 프롬프트 사용 (토큰 절감)

    Returns:
        사용자 프롬프트
    """
    # 카드 정보 포맷팅
    cards_info = []
    for i, card in enumerate(cards, 1):
        position = card.get("position", "UNKNOWN")
        card_code = card.get("card_code", "")
        card_name = card.get("card_name", "")
        card_month = card.get("card_month", 0)
        card_type = card.get("card_type", "")

        # position_label 추출
        position_map = {
            "SELF": "본인/현재 상태",
            "OTHER": "상대/환경",
            "PROCESS": "과정/관계",
            "RESULT": "결과/조언",
        }
        position_label = position_map.get(position, position)

        cards_info.append(
            f"{i}. {position_label}: {card_name} ({card_code}) - {card_month}월 {card_type}"
        )

    cards_text = "\n".join(cards_info)

    # 질문이 있는 경우
    question_text = ""
    if question:
        question_text = f"\n질문: {question}\n"

    # 프롬프트 버전 선택
    schema_instruction = HWATU_SCHEMA_INSTRUCTION_LITE if lite else HWATU_SCHEMA_INSTRUCTION

    return f"""다음 4장의 화투 카드를 보고 점을 쳐주세요. JSON으로 응답하세요.
{question_text}
## 뽑힌 카드 (4장)

{cards_text}

{schema_instruction}

위 스키마에 맞춰 유효한 JSON만 출력하세요.
"""


