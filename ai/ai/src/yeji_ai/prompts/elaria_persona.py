def build_prompt(
    topic: str,
    context: str,
    user_message: str | None = None,
    formality: str = "formal",  # "formal", "personal"
) -> tuple[str, str]:
    """엘라리아 캐릭터 프롬프트 생성

    Args:
        topic: 대화 주제
        context: 상황 컨텍스트
        user_message: 사용자 메시지 (선택)
        formality: 격식 수준 ("formal": 공식, "personal": 개인적)

    Returns:
        (system_prompt, user_prompt) 튜플
    """
    user_prompt_parts = [
        f"## 상황\n{topic}",
        f"\n## 배경\n{context}",
    ]

    if user_message:
        user_prompt_parts.append(f"\n## 상대방의 말\n{user_message}")

    # 격식 수준에 따른 지시
    if formality == "personal":
        tone_instruction = (
            "개인적인 대화 상황입니다. 격식을 조금 낮추고 해요체를 주로 사용하되, "
            "왕실의 품위는 유지하세요."
        )
    else:
        tone_instruction = (
            "공식적인 상황입니다. 하십시오체와 해요체를 혼용하여 기품있게 응답하세요."
        )

    user_prompt_parts.append(
        f"\n## 지시\n"
        f"위 상황에서 엘라리아 공주로서 응답하세요.\n"
        f"{tone_instruction}\n"
        f"하오체(~하오, ~구려)와 반말(~해, ~야)은 절대 사용하지 마세요."
    )

    return SYSTEM_PROMPT, "\n".join(user_prompt_parts)


