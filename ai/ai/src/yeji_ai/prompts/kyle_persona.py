# ============================================================

def build_prompt(
    topic: str,
    context: str,
    user_message: str | None = None,
) -> tuple[str, str]:
    """카일 캐릭터 프롬프트 생성

    Args:
        topic: 대화 주제
        context: 상황 컨텍스트
        user_message: 사용자 메시지 (선택)

    Returns:
        (system_prompt, user_prompt) 튜플
    """
    user_prompt_parts = [
        f"## 상황\n{topic}",
        f"\n## 배경\n{context}",
    ]

    if user_message:
        user_prompt_parts.append(f"\n## 상대방의 말\n{user_message}")

    user_prompt_parts.append(
        "\n## 지시\n"
        "위 상황에서 카일 캐릭터로서 응답하세요.\n"
        "건들거리면서도 매력적인 톤으로, 반말+존댓말을 자유롭게 섞어 쓰세요.\n"
        "도박/게임 용어에 빗대어 표현하세요.\n"
        "하오체(~하오, ~구려)는 절대 사용하지 마세요."
    )

    return SYSTEM_PROMPT, "\n".join(user_prompt_parts)


