def build_prompt(
    topic: str,
    context: str,
    user_message: str | None = None,
    target: str = "general",  # "general", "noble", "junior"
) -> tuple[str, str]:
    """화린 캐릭터 프롬프트 생성

    Args:
        topic: 대화 주제
        context: 상황 컨텍스트
        user_message: 사용자 메시지 (선택)
        target: 대화 상대 ("general", "noble", "junior")

    Returns:
        (system_prompt, user_prompt) 튜플
    """
    user_prompt_parts = [
        f"## 상황\n{topic}",
        f"\n## 배경\n{context}",
    ]

    if user_message:
        user_prompt_parts.append(f"\n## 상대방의 말\n{user_message}")

    # 대화 상대에 따른 지시
    if target in ("soiseol", "junior"):
        tone_instruction = (
            "가까운 동료에게 말하듯 언니처럼 반말 섞어서 다정하게 "
            "(하지만 잔소리도) 응답하세요."
        )
    elif target == "noble":
        tone_instruction = (
            "귀족/왕족에게 말하듯 정중하지만 비즈니스적으로 응답하세요. "
            "'공주님', '도련님' 호칭 사용."
        )
    else:
        tone_instruction = "일반 손님에게 말하듯 나른하면서도 뼈있게 응답하세요."

    user_prompt_parts.append(
        f"\n## 지시\n"
        f"위 상황에서 화린 캐릭터로서 응답하세요.\n"
        f"{tone_instruction}\n\n"
        f"⚠️ 필수 어미: ~해요, ~이에요, ~예요, ~네요, ~어요, ~죠, ~드릴게요, ~할게요\n"
        f"❌ 절대 금지: ~합니다, ~입니다, ~습니다, ~겠습니다, ~하오, ~구려\n"
        f"응답 완료 전 모든 문장이 해요체인지 최종 점검하세요."
    )

    return SYSTEM_PROMPT, "\n".join(user_prompt_parts)


