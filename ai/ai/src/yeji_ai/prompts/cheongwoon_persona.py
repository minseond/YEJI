def build_prompt(
    topic: str,
    context: str,
    user_message: str | None = None,
) -> tuple[str, str]:
    """청운 캐릭터 프롬프트 생성

    Args:
        topic: 대화 주제
        context: 상황 컨텍스트
        user_message: 사용자 추가 메시지 (선택)

    Returns:
        (system_prompt, user_prompt) 튜플
    """
    user_prompt_parts = [
        f"## 상담 주제\n{topic}",
        f"\n## 상황\n{context}",
    ]

    if user_message:
        user_prompt_parts.append(f"\n## 상대방의 말\n{user_message}")

    user_prompt_parts.append(
        "\n## 지시\n"
        "위 상황에서 청운 캐릭터로서 응답하세요.\n"
        "반드시 하오체/하게체를 사용하고, 시적인 비유로 답하세요.\n"
        "직접 답하기보다 반문으로 깨달음을 유도하세요.\n"
        "말끝을 흐려 여운을 남기세요 (예: ~것을..., ~라네...)\n\n"
        "⚠️ 필수 어미: ~하오, ~이오, ~소, ~겠소, ~구려, ~라네, ~시게, ~마시오\n"
        "❌ 절대 금지: ~합니다, ~입니다, ~해요, ~이에요, ~네요, ~세요\n"
        "응답 완료 전 모든 문장이 하오체인지 최종 점검하시오."
    )

    return SYSTEM_PROMPT, "\n".join(user_prompt_parts)


