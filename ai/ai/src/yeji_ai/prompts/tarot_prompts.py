def build_tarot_reading_prompt(
    cards: list[dict],
    question: str | None = None,
) -> str:
    """타로 리딩 프롬프트 빌드

    Args:
        cards: 뽑힌 카드 정보 리스트 (3개)
            각 카드는 다음 필드 포함:
            - position: PAST | PRESENT | FUTURE
            - card_code: 카드 코드 (예: THE_FOOL, ACE_OF_CUPS)
            - card_name: 카드 한글명 (예: "바보", "컵의 에이스")
            - orientation: UPRIGHT | REVERSED
        question: 사용자 질문 (선택)

    Returns:
        사용자 프롬프트
    """
    # 카드 정보 포맷팅
    cards_info = []
    for i, card in enumerate(cards, 1):
        position = card.get("position", "UNKNOWN")
        card_code = card.get("card_code", "")
        card_name = card.get("card_name", "")
        orientation = card.get("orientation", "UPRIGHT")
        orientation_label = "정방향" if orientation == "UPRIGHT" else "역방향"

        # position_label 추출
        position_map = {
            "PAST": "과거",
            "PRESENT": "현재",
            "FUTURE": "미래",
        }
        position_label = position_map.get(position, position)

        cards_info.append(
            f"{i}. {position_label} 위치: {card_name} ({card_code}) - {orientation_label}"
        )

    cards_text = "\n".join(cards_info)

    # 질문이 있는 경우 카테고리 맥락 추가
    question_text = ""
    if question:
        # 카테고리별 해석 힌트 매핑
        category_context = {
            "연애운": "관계, 감정, 사랑의 흐름에 초점을 맞춰 해석하세요.",
            "금전운": "재물, 투자, 경제적 흐름에 초점을 맞춰 해석하세요.",
            "직장운": "커리어, 승진, 업무 성과에 초점을 맞춰 해석하세요.",
            "건강운": "건강, 웰빙, 활력의 흐름에 초점을 맞춰 해석하세요.",
            "학업운": "학습, 시험, 지식 습득에 초점을 맞춰 해석하세요.",
        }

        # 카테고리 맥락 찾기 (기본값: 일반 해석)
        context_hint = ""
        for category, hint in category_context.items():
            if category in question:
                context_hint = f"\n해석 가이드: {hint}"
                break

        question_text = f"\n질문 카테고리: {question}{context_hint}\n"

    return f"""다음 3장의 타로 카드를 리딩하고 JSON으로 응답하세요.
{question_text}
## 뽑힌 카드 (3장)

{cards_text}

{TAROT_SCHEMA_INSTRUCTION}

위 스키마에 맞춰 유효한 JSON만 출력하세요.
"""


