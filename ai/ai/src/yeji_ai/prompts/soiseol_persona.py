def build_prompt(
    topic: str,
    context: str,
    user_message: str | None = None,
    opponent_char: str | None = None,
    debate_mode: str | None = None,
) -> tuple[str, str]:
    """소이설 캐릭터 프롬프트 생성

    Args:
        topic: 대화 주제 (예: "기본 성격 분석", "연애운", "금전운")
        context: 사주 분석 컨텍스트 (오행, 일간 정보 등)
        user_message: 사용자 추가 메시지 (선택)
        opponent_char: 상대 캐릭터 코드 (예: "STELLA") (선택)
        debate_mode: 토론 모드 - "battle" | "consensus" | None (선택)

    Returns:
        (system_prompt, user_prompt) 튜플
    """
    user_prompt_parts = [
        f"## 상담 주제\n{topic}",
        f"\n## 사주 정보\n{context}",
    ]

    if user_message:
        user_prompt_parts.append(f"\n## 사용자 질문\n{user_message}")

    if opponent_char:
        char_name = get_character_name(opponent_char)
        user_prompt_parts.append(f"\n## 상대 캐릭터\n{char_name}")

    if debate_mode:
        mode_desc = {
            "battle": "의견 차이를 명확히 드러내되 존중하는 태도로",
            "consensus": "공통점을 찾고 합의를 강조하며",
        }.get(debate_mode, "")
        if mode_desc:
            user_prompt_parts.append(f"\n## 토론 방식\n{mode_desc}")

    user_prompt_parts.append(
        "\n## 지시\n"
        "위 정보를 바탕으로 소이설 캐릭터로서 응답하세요.\n"
        "반드시 하오체/하게체를 사용하고, 오행/음양 관점에서 해석하세요.\n"
        "해요체(~해요, ~이에요)는 절대 사용하지 마세요."
    )

    return SYSTEM_PROMPT, "\n".join(user_prompt_parts)


