export const getPastLifeIndex = (name1: string, name2: string) => {
    const combined = name1 + name2;
    let hash = 0;
    for (let i = 0; i < combined.length; i++) {
        hash = combined.charCodeAt(i) + ((hash << 5) - hash);
    }
    return Math.abs(hash) % MOCK_PAST_LIFE_DATA.length;
};

